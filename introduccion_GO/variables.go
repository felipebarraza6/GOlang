//variables
package main
import "fmt"

func main(){
	//declarando variable  
	var a string = "empezar"
	fmt.Println(a)
	//Declarando mas de una variable
	var b, c, h int = 4, 5, 0
	fmt.Println(b, c, h)
	//Declarando variables con resultado booleano
	var d = true
	fmt.Println(d)
	//Varibale e de tipo entero
	var e int
	fmt.Println(e)
	//Declaracion de una cadena con otro tipo de sintaxis
	f:= "short"
	fmt.Println(f)
}
