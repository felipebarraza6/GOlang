package main
import "fmt"
func main(){
	//Queremos imprimir cadenas golang
	fmt.Println("go" + "lang")
	//Queremos aprender a sumar/restar enteros
	fmt.Println("5+5 =", 5+5)
	//Queremos aprender a sumar flotantes
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	//Manejo de booleanos
	fmt.Println("resultado=")
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!false)
}
